package if_else;

public class Sintaxe_Simples {

	public static void main(String[] args) {
		
		int x = 5;
		
		System.out.println("Bom dia");
		
		// Condicao verdadeira executa o bloco de codigo
		if(x > 0) {
			System.out.println("Boa tarde");
		}
		
		// Condicao falsa nao executa o bloco de codigo
		if(x < 0) {
			System.out.println("Boa noite");
		}
		

	}

}
